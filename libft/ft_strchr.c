#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	char	*c;

	c = (char *)str;
	while (*c != (char)ch && *c != '\0')
		c++;
	if (*c == (char)ch)
		return (c);
	return (0);
}
