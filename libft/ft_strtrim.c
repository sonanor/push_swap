#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*substr;
	size_t	i;
	size_t	j;

	if (!s1 || !set)
		return (NULL);
	i = 0;
	j = ft_strlen(s1) - 1;
	if (!s1 || !set)
		return (NULL);
	while (s1[i] && ft_strchr(set, s1[i]))
		i++;
	while (j > i && ft_strchr(set, s1[j]))
		j--;
	substr = ft_substr(s1, i, j - i + 1);
	if (!substr)
		return (NULL);
	return (substr);
}
